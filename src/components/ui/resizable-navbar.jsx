export const NavbarButton = ({
  href,
  as: Tag = "a",
  children,
  className,
  variant = "purple-500",
  ...props
}) => {
  const baseStyles =
    "flex px-4 py-2 rounded-md w-full button text-black text-sm font-bold relative cursor-pointer hover:-translate-y-0.5 transition duration-200 inline-block text-center";

  const variantStyles = {
    primary: "shadow-md hover:bg-purple-500/10 bg-purple-500",
    secondary: "bg-transparent shadow-none dark:text-white",
    dark: "bg-black text-white shadow-md",
    gradient: "bg-gradient-to-b from-blue-500 to-blue-700 text-white shadow-inner",
  };

  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};

